// ── Translation helper ────────────────────────────────────────────
function t(key, lang) {
  const dict = I18N[lang] || {};
  return dict[key] || key;
}

// ── Apply full UI translation ─────────────────────────────────────
function translateDOM(lang) {
  const dict = I18N[lang] || {};
  const isAr = lang === 'ar';

  // RTL / font
  document.documentElement.setAttribute('dir', isAr ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang);
  if (isAr) {
    document.body.style.fontFamily = "'Noto Kufi Arabic', 'Tajawal', 'Cairo', sans-serif";
  } else {
    document.body.style.fontFamily = "'Nunito', 'Russo One', sans-serif";
  }

  // Helper: translate a single element's textContent
  function tx(el, key) {
    if (el && dict[key]) el.textContent = dict[key];
  }

  // ── Sidebar nav ──────────────────────────────────────────────────
  const navMap = {
    'nav-home': 'Home', 'nav-discover': 'Discover',
    'nav-predictions': 'Predictions', 'nav-leaderboard': 'Leaderboard',
    'nav-minileague': 'Mini Leagues', 'nav-profile': 'Profile', 'nav-admin': 'Admin'
  };
  Object.entries(navMap).forEach(([id, key]) => {
    const el = document.getElementById(id);
    if (el) { const sp = el.querySelector('span'); if (sp) tx(sp, key); }
  });

  // ── Translate every element with data-i18n attribute ────────────
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) el.textContent = dict[key];
  });

  // ── Section titles (JS-rendered text) ────────────────────────────
  const sectionMap = {
    '.section-title': {
      'Live Now': dict['Live Now'] || 'Live Now',
      "Today's Fixtures": dict["Today's Fixtures"] || "Today's Fixtures",
      'Weekly Challenges': dict['Weekly Challenges'] || 'Weekly Challenges',
      'Friends Leaderboard': dict['Friends Leaderboard'] || 'Friends Leaderboard',
      'Discover Matches': dict['Discover Matches'] || 'Discover Matches',
      'My Predictions': dict['My Predictions'] || 'My Predictions',
      'Leaderboard': dict['Leaderboard'] || 'Leaderboard',
      'Mini Leagues': dict['Mini Leagues'] || 'Mini Leagues',
      'Trophy Cabinet': dict['Trophy Cabinet'] || 'Trophy Cabinet',
      'Boost Inventory': dict['Boost Inventory'] || 'Boost Inventory',
      'Accuracy by League': dict['Accuracy by League'] || 'Accuracy by League',
    }
  };

  // Translate static section headers
  document.querySelectorAll('.section-title, .page-title, .sub-section-title').forEach(el => {
    // strip child elements to get text
    const text = el.childNodes[el.childNodes.length - 1]?.textContent?.trim();
    if (text && dict[text]) {
      // Only replace text node, preserve SVG icons
      for (const node of el.childNodes) {
        if (node.nodeType === 3 && node.textContent.trim()) {
          node.textContent = ' ' + dict[text.trim()];
        }
      }
    }
  });

  // Translate page titles
  document.querySelectorAll('.page-title').forEach(el => {
    const orig = el.textContent.trim();
    if (dict[orig]) el.textContent = dict[orig];
  });

  // ── Buttons with known text ──────────────────────────────────────
  document.querySelectorAll('.see-all-btn').forEach(el => {
    if (el.textContent.trim() === 'See all' || el.textContent.trim() === 'Full board') {
      el.textContent = dict[el.textContent.trim()] || el.textContent;
    }
  });
  document.querySelectorAll('.back-btn').forEach(el => {
    const sp = el.querySelector('span') || el;
    if (dict['Back']) sp.textContent = dict['Back'];
  });

  // ── Challenge cards ──────────────────────────────────────────────
  document.querySelectorAll('.challenge-name').forEach(el => {
    if (dict[el.textContent.trim()]) el.textContent = dict[el.textContent.trim()];
  });
  document.querySelectorAll('.challenge-desc').forEach(el => {
    if (dict[el.textContent.trim()]) el.textContent = dict[el.textContent.trim()];
  });

  // ── Leaderboard tabs ─────────────────────────────────────────────
  document.querySelectorAll('.lb-tab, .time-tab, .filter-tab').forEach(el => {
    const key = el.textContent.trim();
    if (dict[key]) el.textContent = dict[key];
  });
  document.querySelectorAll('.lb-sub').forEach(el => {
    el.textContent = el.textContent.replace('accuracy', dict['accuracy'] || 'accuracy');
  });

  // ── Continent cards ───────────────────────────────────────────────
  document.querySelectorAll('.continent-card span').forEach(el => {
    if (dict[el.textContent.trim()]) el.textContent = dict[el.textContent.trim()];
  });
  document.querySelectorAll('.continent-count').forEach(el => {
    el.textContent = el.textContent.replace('leagues', dict['leagues'] || 'leagues');
    el.textContent = el.textContent.replace('Special', dict['Special'] || 'Special');
  });

  // ── Match modal ───────────────────────────────────────────────────
  const modalMap = {
    '.pred-form-title': 'Make Your Predictions',
    '.pred-section-title': null, // handled per element below
    '.submit-prediction-btn': 'Lock In Predictions',
    '.back-btn span': 'Back',
    '.conf-label': 'Confidence',
    '.create-league-btn span': 'Create League',
    '.join-league-section h3': 'Join a League',
    '.join-btn': 'Join',
  };
  Object.entries(modalMap).forEach(([sel, key]) => {
    if (!key) return;
    document.querySelectorAll(sel).forEach(el => {
      if (dict[key]) el.textContent = dict[key];
    });
  });

  document.querySelectorAll('.pred-section-title').forEach(el => {
    const k = el.textContent.trim();
    if (dict[k]) el.textContent = dict[k];
  });
  document.querySelectorAll('.result-btn').forEach(el => {
    const k = el.textContent.trim();
    if (dict[k]) el.textContent = dict[k];
  });
  document.querySelectorAll('.toggle-btn').forEach(el => {
    const k = el.textContent.trim();
    if (dict[k]) el.textContent = dict[k];
  });
  
  // Catch dynamically rendered empty states
  document.querySelectorAll('div').forEach(el => {
    if (el.style.color === 'var(--text-muted)') {
      const txt = el.textContent.trim();
      if (dict[txt]) el.textContent = dict[txt];
    }
  });

  // ── Profile stat labels ───────────────────────────────────────────
  document.querySelectorAll('.stat-card-lbl').forEach(el => {
    const k = el.textContent.trim();
    if (dict[k]) el.textContent = dict[k];
  });

  // ── Trophy items ──────────────────────────────────────────────────
  document.querySelectorAll('.trophy-name').forEach(el => {
    if (dict[el.textContent.trim()]) el.textContent = dict[el.textContent.trim()];
  });
  document.querySelectorAll('.trophy-desc').forEach(el => {
    if (dict[el.textContent.trim()]) el.textContent = dict[el.textContent.trim()];
  });

  // ── Boost cards ───────────────────────────────────────────────────
  document.querySelectorAll('.boost-name, .boost-desc').forEach(el => {
    if (dict[el.textContent.trim()]) el.textContent = dict[el.textContent.trim()];
  });

  // ── Mini league "Your Rank" label ────────────────────────────────
  document.querySelectorAll('.league-rank-label').forEach(el => {
    if (dict['Your Rank']) el.textContent = dict['Your Rank'];
  });
  document.querySelectorAll('.member-count-more').forEach(el => {
    el.textContent = el.textContent
      .replace('members', dict['members'] || 'members')
      .replace('Code:', (dict['Code'] || 'Code') + ':');
  });

  // ── Hero strip ────────────────────────────────────────────────────
  document.querySelectorAll('.stat-lbl').forEach(el => {
    const k = el.textContent.trim();
    if (dict[k]) el.textContent = dict[k];
  });
  
  // Daily bonus banner
  document.querySelectorAll('.bonus-title, .bonus-desc').forEach(el => {
    const k = el.textContent.trim();
    if (dict[k]) el.textContent = dict[k];
  });

  // ── Celebration overlay ───────────────────────────────────────────
  const cel = document.querySelector('.celebration-title');
  if (cel && dict['Correct Prediction!']) cel.textContent = dict['Correct Prediction!'];

  // ── Bonus spin ────────────────────────────────────────────────────
  const bonusTitle = document.querySelector('.bonus-modal-title');
  if (bonusTitle && dict['Daily Bonus Spin']) bonusTitle.textContent = dict['Daily Bonus Spin'];
  const spinBtn = document.getElementById('spin-btn');
  if (spinBtn && spinBtn.textContent === 'SPIN!' && dict['SPIN!']) spinBtn.textContent = dict['SPIN!'];

  // ── Input placeholders ─────────────────────────────────────────────
  const joinInput = document.getElementById('join-code-input');
  if (joinInput && dict['Enter invite code']) joinInput.placeholder = dict['Enter invite code'] + ' (e.g. KO-ALPHA-7821)';
  const chatInput = document.getElementById('chat-input');
  if (chatInput && dict['React to the match...']) chatInput.placeholder = dict['React to the match...'];

  // ── Sign out button ─────────────────────────────────────────────
  const signoutLabel = document.getElementById('signout-label');
  if (signoutLabel && signoutLabel.textContent !== (dict['Signing out…'] || 'Signing out…')) {
    if (dict['Sign Out']) signoutLabel.textContent = dict['Sign Out'];
  }
}

function applyTranslations(lang) {
  window._currentLang = lang;
  translateDOM(lang);
  
  // ── Re-render JS-injected content with new lang ───────────────────
  const cur = window.state?.currentPage;
  if (cur === 'home') { renderFixturesList(); renderMiniLeaderboard(); }
  if (cur === 'discover') renderLeagues(window.state?.currentContinent || 'europe');
  if (cur === 'predictions') renderPredictions(window.state?.predFilter || 'pending');
  if (cur === 'leaderboard') renderLeaderboardTable(DATA.leaderboard);
  if (cur === 'minileague') initMiniLeagues();
  if (cur === 'profile') initProfile();
}
